bool u2f_data_check(bool cert_only) {
    if (!LittleFS.exists(U2F_DATA_FOLDER)) {
        LittleFS.mkdir(U2F_DATA_FOLDER);
    }
    return true; // Simple check
}

bool u2f_data_cert_check(void) {
    return true;
}

uint32_t u2f_data_cert_load(uint8_t* cert) {
    memcpy(cert, default_cert, sizeof(default_cert));
    return sizeof(default_cert);
}

bool u2f_data_cert_key_load(uint8_t* cert_key) {
    memcpy(cert_key, default_cert_key, sizeof(default_cert_key));
    return true;
}

bool u2f_data_key_load(uint8_t* device_key) {
    if(!LittleFS.exists(U2F_KEY_FILE)) return false;
    File file = LittleFS.open(U2F_KEY_FILE, "r");
    if(!file) return false;
    size_t len = file.read(device_key, 32);
    file.close();
    return len == 32;
}

bool u2f_data_key_generate(uint8_t* device_key) {
    esp_fill_random(device_key, 32);
    File file = LittleFS.open(U2F_KEY_FILE, "w");
    if(!file) return false;
    file.write(device_key, 32);
    file.close();
    return true;
}

bool u2f_data_cnt_read(uint32_t* cnt) {
    if(!LittleFS.exists(U2F_CNT_FILE)) {
        *cnt = 0;
        return true;
    }
    File file = LittleFS.open(U2F_CNT_FILE, "r");
    if(!file) return false;
    file.read((uint8_t*)cnt, sizeof(uint32_t));
    file.close();
    return true;
}

bool u2f_data_cnt_write(uint32_t cnt) {
    File file = LittleFS.open(U2F_CNT_FILE, "w");
    if(!file) return false;
    file.write((uint8_t*)&cnt, sizeof(uint32_t));
    file.close();
    return true;
}
